// Adição 
console.log(2+2);

//subtração
console.log(10-5);

//multiplicação
console.log(10*5);

//divisão
console.log(10/2);

//incremento
let numero = 10;
console.log(++numero);

//decremento
console.log(--numero);

//comparação maior
console.log(10 > 5);

//comparação de menor
console.log(10 < 5);

//comparação de menor ou igual
console.log(3 <= 2);

//comparação de maior ou igual 
console.log(20 >= 14);

//comparação de igualdade
console.log("Olá" == "Olá");

//comparação de igualdade rigido
console.log(5 === "5"); // Dois == compara os valores e Três === compara tbm o tipo!

//comparação de diferente
console.log("olá" != "oi");

//comparação de diferente rigido
console.log(10 !== "10"); // Dois != compara os valores se são diferente e Três !== compara tbm o tipo se são diferente!

//comparador lógico &&
console.log(10 > 5 && 8 < 10 && 11 > 5); // && Todas condições tem que dar verdade!

//comparador lógico OR ||
console.log(10 < 5 || 8 > 10); // || Pelo menos uma das condições tem que dar verdade!

//comparador de negação !
console.log(!5 > 3); // ! Negação altera o valor para o contrario.

//operador de concatenar
let nome = "Joana"
let sobrenome = "D'Ark"
console.log(nome + " " + sobrenome);